.MODEL SMALL
.DATA
GRADES  DW 19B2H,20C3H, 69FFH, BC55H, AF99H
HIGHEST DW ?
.CODE
MAIN:
    MOV AX, @DATA
    MOV DS, AX

    MOV SI, OFFSET GRADES
    MOV AX, [SI]
    MOV CX, 4

NEXT:
    INC SI
    CMP [SI], AL
    JBE SKIP
    MOV AX, [SI]
SKIP:
    LOOP NEXT

    MOV HIGHEST, AX

    MOV AH, 4CH
    INT 21H
END MAIN
