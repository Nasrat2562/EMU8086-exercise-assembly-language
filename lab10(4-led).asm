org 100h

   JPIPC_C EQU 1FH
JPIPA   EQU 19H
JPIPB   EQU 1BH
JPIPC   EQU 1DH

MOV AL, 10000000B
OUT JPIPC_C, AL

MOV AL, 11111111B
OUT JPIPA, AL

MOV AL, 00000000B
OUT JPIPC, AL

START:
MOV AL, 00000001B
OUT JPIPB, AL
CALL DELAY

MOV AL, 00000010B
OUT JPIPB, AL
CALL DELAY

MOV AL, 00000100B
OUT JPIPB, AL
CALL DELAY

MOV AL, 00001000B
OUT JPIPB, AL
CALL DELAY

JMP START

DELAY:
MOV CX, 0FFFFH
WAIT1:
NOP
LOOP WAIT1
RET


ret




